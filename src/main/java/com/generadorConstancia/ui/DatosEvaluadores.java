    private void showEvaluadorData(int index, JTextField cedula, JTextField grado){
         
        DocenteModel dm = evaluadores.get(index);
        cedula.setText(dm.getCedula());
        grado.setText(dm.getGrado());
        
       
    }
    
    public List<DocenteModel> evaluadoresList(){
        
        List<DocenteModel> temp = new ArrayList<>();
        List<JComboBox> combos = new ArrayList<>();
        
        combos.add(comboNombreEvaluador1);
        combos.add(comboNombreEvaluador2);
        combos.add(comboNombreEvaluador3);
        
        combos.forEach((JComboBox combo) ->{
        
            DocenteModel dm = evaluadores.get(combo.getSelectedIndex());
            temp.add(dm);
        
        });
        
        return temp;
    }
    
    
    

