    private void getEvaluadores(){
        
        DocenteServices es = new DocenteServices();
        evaluadores = es.getEvaluadores();
        
        if(evaluadores.isEmpty()){
            JOptionPane.showMessageDialog(this,"Hubo un problema al cargar los datos");
            
        }else{
            evaluadores.forEach((DocenteModel dm) ->{
                comboNombreEvaluador1.addItem(dm.getNombre());
                comboNombreEvaluador2.addItem(dm.getNombre());
                comboNombreEvaluador3.addItem(dm.getNombre());
            });
        }
        
    }
    
    
